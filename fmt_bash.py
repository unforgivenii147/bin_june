def get_nobinary(path: (str | Path)) -> list[Path]:
    return [f for f in get_files(path) if not is_binary(f)]


def get_files(path: str | Path, include_hidden: bool = True, ext: list[str] | None = None) -> list[Path]:
    path = Path(path)
    if not path.exists():
        raise FileNotFoundError(f"Path does not exist: {path}")
    if not path.is_dir():
        raise NotADirectoryError(f"Path is not a directory: {path}")

    ext = tuple(ext) if ext else None
    files = []
    stack = [path]

    while stack:
        current = stack.pop()
        try:
            with os_scandir(current) as entries:
                for entry in entries:
                    if entry.is_symlink():
                        continue
                    if entry.is_dir(follow_symlinks=False):
                        if entry.name not in SKIP_DIRS:
                            stack.append(entry)
                    elif entry.is_file(follow_symlinks=False):
                        if not include_hidden and entry.name.startswith("."):
                            continue
                        if ext is None or entry.name.endswith(ext):
                            files.append(Path(entry.path))
        except (PermissionError, OSError):
            continue

    return sorted(files)


def has_shell_shebang(path: Path) -> bool:
    try:
        with Path(path).open("rb") as f:
            first = f.readline(256).decode("utf-8", errors="ignore").strip()
    except Exception:
        return False
    if not first.startswith("#!"):
        return False
    return "bash" in first or "sh" in first


def process_file(path) -> tuple[bool, Path]:
    path = Path(path)
    print(f"Formatting:  {path.name}")
    try:
        res, _, _ = runcmd(["shfmt", "-w", str(path)], show_output=True)
        if res != 0:
            print("  shfmt failed:", res.stderr.strip(), file=sys.stderr)
            return False, path
    except Exception as e:
        print("  error running shfmt:", e, file=sys.stderr)
        return False, path
    return True, path


def main() -> None:
    cwd = Path.cwd()
    files = [p for p in get_nobinary(cwd) if not p.suffix and has_shell_shebang(p) or p.suffix == ".sh"]
    results = mpf3(process_file, files)
    for res in results:
        ret, k = res
        if not ret:
            print(f"  - {k.relative_to(cwd)}")


if __name__ == "__main__":
    main()
